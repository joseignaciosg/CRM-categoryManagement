


<h2>Acerca de la herramieta de CRM</h2>

<p>Esta herramienta constituye una forma simple de acceder a la
	informacíon de los clicks que sus suscriptores realizaron en cada una
	de sus campañas. Para su uso, siga los siguientes pasos:</p>
<ol>
	<li>Ingrese su nombre y contraseña en esquina superior derecha de la
		pantalla. (Estas credenciales serán subministradas en un primer
		momento por Category Management Inc.)</li>
	<li>Presione "Entrar"</li>
	<li>Si las credenciales fueron correctamente insertadas , el sistema le
		informará que se ha logueado correctamente.</li>
	<li>Una vez logeado, el formulario de logueo será reemplazado por un
		botón con su nombre de usuario, haga click sobre él para ver las
		opciones con la que cuenta.</li>
	<li>Desde el menu mencionado en el paso anterior podrá acceder al
		"Visor de Clicks" y podrá ir al panel de administración de su cuenta
		(Se recomienda que cambie la contraseña de su cuenta desde este
		panel).</li>
</ol>